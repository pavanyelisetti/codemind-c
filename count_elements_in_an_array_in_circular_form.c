#include<stdio.h>
int main()
{
    int n;
    scanf("%d",&n);
    int a[100],i,c=0;
    for(i=0;i<n;i++)
    {
        scanf("%d",&a[i]);
    }
    for(i=1;i<n-1;i++)
    {
        if((a[i-1]%2==0 && a[i+1]%2==1) || (a[i-1]%2==1 && a[i+1]%2==0))
        {
            c+=1;
        }
    }
    if((a[1]%2==0 && a[n-1]%2==1) || (a[1]%2==1 && a[n-1]%2==0))
    {
        c+=1;
    }
    if((a[n-2]%2==0 && a[0]%2==1) || (a[n-2]%2==1 && a[0]%2==0))
    {
        c+=1;
    }
    printf("%d",c);
}